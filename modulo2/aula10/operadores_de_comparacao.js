// Operador de Igualdade ( == )
console.log(1 == 1)

// Operadore de Diferente ( != )
console.log(1 != 1)
console.log(1 != 2)

// Operador de Maior que ( > )
console.log(2 > 1)
console.log(1 > 2)

// Operador Maior ou Igual ( >= )
console.log(2 >= 1)
console.log(2 >= 2)

// Operador de Menor que ( < )
console.log(1 < 1)
console.log(1 < 2)

// Operador de Menor ou Igual que ( <= )
console.log(1 <= 1)
console.log(1 <= 2)